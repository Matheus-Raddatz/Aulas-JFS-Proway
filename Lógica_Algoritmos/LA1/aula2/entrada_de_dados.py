# todos os valores input vêm em forma de str, portanto:
valor_a = input("Digite o valor 'a': ")
valor_b = input("Digite o valor 'b': ")

soma = valor_a + valor_b
print(soma) # se valor_a = 2 e valor_b = 3, aparecerá 23

valor_c = int(input("Digite o valor 'c': ")) # conversão de str para int
valor_d = int(input("Digite o valor 'd': ")) # //=//=//= // =// =//= //=

soma_2 = valor_c + valor_d
print(soma_2) # assim, com os mesmos valores, aparecerá 5